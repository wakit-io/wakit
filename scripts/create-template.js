#!/usr/bin/env node
/**
 * 새 템플릿 생성 스크립트
 *  - 베이스 템플릿을 복사 + 리브랜딩 + package.json 스크립트 등록 + (web/ 있으면) npm install
 *  - 선택: Supabase 스키마/버킷/마이그레이션 자동 생성
 *
 * 사용:
 *   npm run create:template                                  # 대화형
 *   npm run create:template -- app_shop --base app_sveltekit # 비대화(이름+베이스)
 *   npm run create:template -- app_shop --base app_astro --color "#10b981" --no-db
 *
 * --base: app_basic(기본) | app_astro | app_sveltekit | (templates/ 내 임의 템플릿)
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');
const { execSync } = require('child_process');

const ROOT = path.resolve(__dirname, '..');
const TEMPLATES_DIR = path.join(ROOT, 'templates');
const DEFAULT_BASE = 'app_basic';   // 골든 base 템플릿
const DEFAULT_COLOR = '#6366f1';

// Known base display names (can't be derived from the slug reliably, e.g. "SvelteKit").
const BASE_DISPLAY = { app_basic: 'App Basic', app_astro: 'App Astro', app_sveltekit: 'App SvelteKit' };

// Files copied verbatim (binary or not worth rebranding) — everything else is read as
// UTF-8 and rebranded. Reading a binary (e.g. favicon.ico) as UTF-8 would corrupt it.
const TEXT_EXT = /\.(html|htm|css|js|mjs|cjs|ts|tsx|jsx|json|svg|md|txt|astro|svelte|map|xml|webmanifest|env|gitignore)$/i;

// Directory names skipped anywhere.
const SKIP_NAMES = new Set(['node_modules', '.astro', '.svelte-kit', 'dist']);

function titleCase(slug) {
  return slug.split(/[_-]/).filter(Boolean).map(w => w[0].toUpperCase() + w.slice(1)).join(' ');
}

function parseArgs(argv) {
  const flags = {};
  const positionals = [];
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--base') flags.base = argv[++i];
    else if (a.startsWith('--base=')) flags.base = a.slice('--base='.length);
    else if (a === '--color') flags.color = argv[++i];
    else if (a.startsWith('--color=')) flags.color = a.slice('--color='.length);
    else if (a === '--no-db') flags.db = false;
    else if (a === '--db') flags.db = true;
    else if (!a.startsWith('--')) positionals.push(a);
  }
  return { flags, positionals };
}

/**
 * Recursively copy `src` → `dest`, rebranding text files via `rebrand(content)`.
 * Skips build artifacts (node_modules/.astro/.svelte-kit/dist) and webpack-generated
 * web assets (web/public/{app,wakit,service-worker.js}).
 */
function copyTree(srcRoot, src, dest, rebrand) {
  fs.mkdirSync(dest, { recursive: true });
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    if (SKIP_NAMES.has(entry.name)) continue;
    const srcPath = path.join(src, entry.name);
    const destPath = path.join(dest, entry.name);
    const rel = path.relative(srcRoot, srcPath).split(path.sep).join('/');
    // Skip generated web-layer output (regenerated by the build).
    if (rel === 'web/public/app' || rel === 'web/public/wakit' || rel === 'web/public/service-worker.js') continue;

    if (entry.isDirectory()) {
      copyTree(srcRoot, srcPath, destPath, rebrand);
    } else if (TEXT_EXT.test(entry.name)) {
      fs.writeFileSync(destPath, rebrand(fs.readFileSync(srcPath, 'utf8')));
    } else {
      fs.copyFileSync(srcPath, destPath); // binary: copy verbatim
    }
  }
}

function createSchema(schemaName) {
  try {
    execSync(`supabase db query --local "CREATE SCHEMA IF NOT EXISTS ${schemaName};"`, { cwd: ROOT, stdio: 'pipe' });
    execSync(`supabase db query --local "GRANT USAGE ON SCHEMA ${schemaName} TO anon, authenticated;"`, { cwd: ROOT, stdio: 'pipe' });
    return true;
  } catch {
    return false;
  }
}

function createBucket(bucketName) {
  try {
    const status = execSync('supabase status 2>/dev/null', { cwd: ROOT, encoding: 'utf8' });
    const serviceKey = (status.match(/sb_secret_\S+/) || [])[0];
    if (!serviceKey) return false;
    execSync(
      `curl -s -X POST "http://127.0.0.1:54321/storage/v1/bucket" \
        -H "Authorization: Bearer ${serviceKey}" \
        -H "apikey: ${serviceKey}" \
        -H "Content-Type: application/json" \
        -d '{"id":"${bucketName}","name":"${bucketName}","public":false}'`,
      { cwd: ROOT, stdio: 'pipe' }
    );
    return true;
  } catch {
    return false;
  }
}

function addMigration(schemaName) {
  const ts = new Date().toISOString().replace(/\D/g, '').slice(0, 14);
  const migFile = path.join(ROOT, 'supabase', 'migrations', `${ts}_create_${schemaName}.sql`);
  const sql = [
    `-- ${schemaName} 스키마 생성`,
    `CREATE SCHEMA IF NOT EXISTS ${schemaName};`,
    `GRANT USAGE ON SCHEMA ${schemaName} TO anon, authenticated;`,
    ``,
    `-- ${schemaName} 스토리지 버킷 생성`,
    `INSERT INTO storage.buckets (id, name, public)`,
    `VALUES ('${schemaName}', '${schemaName}', false)`,
    `ON CONFLICT (id) DO NOTHING;`,
    ``,
  ].join('\n');
  fs.writeFileSync(migFile, sql);
  return migFile;
}

async function main() {
  const { flags, positionals } = parseArgs(process.argv.slice(2));
  const nonInteractive = Boolean(positionals[0] && flags.base);

  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  const question = (q) => new Promise(resolve => rl.question(q, resolve));

  try {
    console.log('\n🚀 새 템플릿 생성\n');

    // --- name ---
    let name = positionals[0];
    if (!name) name = (await question('템플릿 이름 (예: app_shop): ')).trim();
    if (!name) { console.log('❌ 이름을 입력해주세요.'); return; }

    const templateDir = path.join(TEMPLATES_DIR, name);
    if (fs.existsSync(templateDir)) { console.log(`❌ 이미 존재하는 템플릿입니다: ${name}`); return; }

    // --- base ---
    let base = flags.base;
    if (!base && !nonInteractive) {
      base = (await question(`베이스 템플릿 (app_basic / app_astro / app_sveltekit) [${DEFAULT_BASE}]: `)).trim();
    }
    base = base || DEFAULT_BASE;
    const baseDir = path.join(TEMPLATES_DIR, base);
    if (!fs.existsSync(baseDir)) {
      const avail = fs.readdirSync(TEMPLATES_DIR, { withFileTypes: true }).filter(d => d.isDirectory()).map(d => d.name);
      console.log(`❌ 베이스 템플릿을 찾을 수 없습니다: ${base}\n   사용 가능: ${avail.join(', ')}`);
      return;
    }

    // --- color / db ---
    let primaryColor = flags.color;
    if (primaryColor === undefined && !nonInteractive) {
      primaryColor = (await question(`브랜드 색상 [${DEFAULT_COLOR}]: `)).trim();
    }
    primaryColor = primaryColor || DEFAULT_COLOR;

    let withDb = flags.db;
    if (withDb === undefined && !nonInteractive) {
      withDb = (await question('Supabase 스키마 + 버킷 자동 생성? (Y/n): ')).trim().toLowerCase() !== 'n';
    }
    if (withDb === undefined) withDb = true;

    // --- rebrand function (slug + hyphen BEM prefix + display name) ---
    const hyphenFrom = base.replace(/_/g, '-') + '-';
    const hyphenTo = name.replace(/_/g, '-') + '-';
    const baseDisplay = BASE_DISPLAY[base] || titleCase(base);
    const newDisplay = titleCase(name);
    const rebrand = (content) =>
      content.split(base).join(name)               // app_sveltekit → app_shop
             .split(hyphenFrom).join(hyphenTo)      // app-sveltekit- → app-shop-  (BEM)
             .split(baseDisplay).join(newDisplay);  // "App SvelteKit" → "App Shop"

    console.log(`\n📁 [${base}] → templates/${name}/ 복사 중...`);
    copyTree(baseDir, baseDir, templateDir, rebrand);

    // wakitConfig.json primaryColor 업데이트
    const configPath = path.join(templateDir, 'wakitConfig.json');
    if (fs.existsSync(configPath)) {
      const config = JSON.parse(fs.readFileSync(configPath, 'utf8'));
      if (config.theme) config.theme.primaryColor = primaryColor;
      fs.writeFileSync(configPath, JSON.stringify(config, null, 2) + '\n');
    }
    console.log(`✅ 템플릿 생성 완료: templates/${name}/ (base: ${base})`);

    // package.json 은 건드리지 않는다. 모든 템플릿은 제네릭 러너(scripts/wakit.js)로 실행.
    //   → 새 템플릿마다 dev:/build:/package: 스크립트를 추가/삭제할 필요가 없다.

    // web/ 레이어가 있으면 의존성 설치
    const webDir = path.join(templateDir, 'web');
    const hasWeb = fs.existsSync(webDir);
    if (hasWeb) {
      console.log('📦 web/ 의존성 설치 중 (npm install)...');
      try {
        execSync('npm install', { cwd: webDir, stdio: 'pipe' });
        console.log('✅ web/ 의존성 설치 완료');
      } catch {
        console.log('⚠️  npm install 실패 - 수동으로 `cd templates/' + name + '/web && npm install` 해주세요');
      }
    }

    if (withDb) {
      console.log('\n🗄️  Supabase 스키마 생성 중...');
      console.log(createSchema(name) ? `✅ 스키마 생성 완료: ${name}` : '⚠️  스키마 적용 실패 (Supabase 실행 중인지 확인)');
      console.log('📦 스토리지 버킷 생성 중...');
      console.log(createBucket(name) ? `✅ 버킷 생성 완료: ${name}` : '⚠️  버킷 생성 실패');
      const migPath = addMigration(name);
      console.log(`📄 마이그레이션 파일: ${path.relative(ROOT, migPath)}`);
    }

    console.log(`\n🎉 완료! (package.json 변경 없음 — 제네릭 러너로 실행)`);
    if (hasWeb) {
      console.log(`   웹+앱 개발 (web 레이어 서버가 /app·/wakit 까지 서빙):`);
      console.log(`     node scripts/wakit.js web ${name}`);
      console.log(`   앱만 (webpack):  node scripts/wakit.js dev ${name}`);
    } else {
      console.log(`   개발 서버:  node scripts/wakit.js dev ${name}   (→ http://localhost:5173/app/app.html)`);
    }
    console.log(`   빌드:       node scripts/wakit.js build ${name}`);
    console.log(`   패키징:     node scripts/wakit.js package ${name}\n`);
  } finally {
    rl.close();
  }
}

main();
